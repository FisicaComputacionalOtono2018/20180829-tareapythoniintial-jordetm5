a=float(input(" dame el valor de a: "))
b=float(input(" dame el valor de b: "))
c=float(input(" dame el valor de c: "))
d=float(input(" dame el valor de d: "))
e=float(input(" dame el valor de e: "))
f=float(input(" dame el valor de f: "))

